func testInlineLOBEnvelope(seq byte, payload []byte) []byte {
	raw := make([]byte, 13+len(payload))
	raw[0] = 0x01
	raw[1] = 0x27
	raw[2] = 0x04
	raw[3] = seq
	binary.LittleEndian.PutUint32(raw[9:13], uint32(len(payload)))
	copy(raw[13:], payload)
	return raw
}

