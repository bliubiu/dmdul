func TestSplitDataOutputRouterLimitsOpenFilesAndReopensForAppend(t *testing.T) {
	dir := t.TempDir()
	tables := make(map[uint32]dataTableInfo)
	for i := uint32(1); i <= maxOpenSplitDataFiles+8; i++ {
		tables[i] = dataTableInfo{table: dictionaryObject{ID: i, Owner: "APP", Name: fmt.Sprintf("T_%02d", i)}}
	}
	router, err := newDataOutputRouter(DataExportOptions{
		TableOutputPath: func(_ string, table string, _ uint32) string {
			return filepath.Join(dir, table+"_data.sql")
		},
	}, "sql", tables)
	if err != nil {
		t.Fatalf("newDataOutputRouter failed: %v", err)
	}
	for i := uint32(1); i <= maxOpenSplitDataFiles+8; i++ {
		if err := router.writeRow(tables[i], fmt.Sprintf("ROW_%02d", i), nil); err != nil {
			t.Fatalf("writeRow(%d) failed: %v", i, err)
		}
		if len(router.filesByTable) > maxOpenSplitDataFiles {
			t.Fatalf("open files = %d, limit = %d", len(router.filesByTable), maxOpenSplitDataFiles)
		}
	}
	if err := router.writeRow(tables[1], "ROW_01_AGAIN", nil); err != nil {
		t.Fatalf("reopened writeRow failed: %v", err)
	}
	if err := router.close(); err != nil {
		t.Fatalf("router.close failed: %v", err)
	}
	raw, err := os.ReadFile(filepath.Join(dir, "T_01_data.sql"))
	if err != nil {
		t.Fatalf("ReadFile failed: %v", err)
	}
	text := string(raw)
	if strings.Count(text, "Generated by dmdul export-data") != 1 {
		t.Fatalf("SQL header should be written once, got %q", text)
	}
	for _, want := range []string{"ROW_01", "ROW_01_AGAIN"} {
		if !strings.Contains(text, want) {
			t.Fatalf("split SQL should contain %q, got %q", want, text)
		}
	}
}

